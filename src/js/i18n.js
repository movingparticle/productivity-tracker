// Pluralize helper: "1 item | 2 items" format
function plural(key, n) {
  const str = t(key);
  if (!str.includes('|')) return str.replace('{n}', n);
  const [one, many] = str.split('|').map(s => s.trim());
  return (n === 1 ? one : many).replace('{n}', n);
}
export { plural };

export function getLang() {
  return localStorage.getItem('prodTrackerLang') || 'es';
}

export function t(key, vars) {
  const lang = getLang();
  let val = (T[lang] || T.es)[key] ?? T.es[key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      val = val.replaceAll('{' + k + '}', v);
    });
  }
  return val;
}

export function applyLang(lang) {
  localStorage.setItem('prodTrackerLang', lang);
  document.documentElement.lang = lang;
  const dict = T[lang] || T.es;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = dict[key] ?? T.es[key];
    if (val == null) return;
    if (el.children.length > 0) {
      for (let i = el.childNodes.length - 1; i >= 0; i--) {
        const n = el.childNodes[i];
        if (n.nodeType === Node.TEXT_NODE && n.textContent.trim()) {
          n.textContent = ' ' + val;
          break;
        }
      }
    } else {
      el.textContent = val;
    }

    // Translate data-q attribute if a corresponding query key exists (e.g. ai.chip.1.q)
    const qKey = key + '.q';
    const qVal = dict[qKey] ?? T.es[qKey];
    if (qVal != null) {
      el.setAttribute('data-q', qVal);
    }
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const val = dict[el.dataset.i18nPh] ?? T.es[el.dataset.i18nPh];
    if (val != null) el.placeholder = val;
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    const val = dict[key] ?? T.es[key];
    if (val != null) el.title = val;
  });
}

export function initI18n() {
  applyLang(getLang());
}
